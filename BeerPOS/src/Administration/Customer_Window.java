    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        
        if(!txtCustomer.getText().equals("") && !txtAddress.getText().equals("") && !txtPhone.getText().equals(""))
        {
            customer.setName(txtCustomer.getText());
            customer.setAddress(txtAddress.getText());
            customer.setPhone(txtPhone.getText());
            
             if(btnInsert.getText().equals("Guardar")){
                 
                 if(customer.InsertCustomer())
                 {
                     JOptionPane.showMessageDialog(this, "Registro exitoso");
                     Modelo = customer.showCustomers(Modelo);
                 }
                 else
                 {
                     JOptionPane.showMessageDialog(this,"Oops, Error al insertar el cliente");
                 }
             }else
             {
                 customer.UpdateCustomer();
                 Modelo = customer.showCustomers(Modelo);
             }
             
             txtCustomer.setText("");
             txtAddress.setText("");
             txtPhone.setText("");
        }
    }//GEN-LAST:event_btnInsertActionPerformed

        public void frm_popUp()
        {
            
            JPopupMenu popupMenu = new JPopupMenu();
            JMenuItem menuItem1 = new JMenuItem("Eliminar");
            JMenuItem menuItem2 = new JMenuItem("Editar");
            
            menuItem1.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    int selectRow = tblCustomers.getSelectedRow(); 
                    if(selectRow != -1){
                        int id = Integer.parseInt(tblCustomers.getValueAt(selectRow,0).toString()); 
                        if(customer.DeleteCustomer(id))
                        {
                            JOptionPane.showMessageDialog(null, "Producto Eliminado correctamente");
                            Modelo = customer.showCustomers(Modelo);
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null,"Oops se encontr√≥ un error al borrar el usuario");
                        }
                    }
                }
            });
            menuItem2.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    int selectRow = tblCustomers.getSelectedRow();
                    if(selectRow != -1){
                        int id = Integer.parseInt(tblCustomers.getValueAt(selectRow,0).toString());
                        String customerStr = String.valueOf(tblCustomers.getValueAt(selectRow,1));  
                        String customerAdd = String.valueOf(tblCustomers.getValueAt(selectRow,2));
                        String customerPhone = String.valueOf(tblCustomers.getValueAt(selectRow,3));


                        txtCustomer.setText(customerStr);
                        txtAddress.setText(customerAdd);
                        txtPhone.setText(customerPhone);
                        customer.setId(id);
                        btnInsert.setText("Actualizar");
                    }
                }
            });
            popupMenu.add(menuItem1);
            popupMenu.add(menuItem2);
            tblCustomers.setComponentPopupMenu(popupMenu);
        }
